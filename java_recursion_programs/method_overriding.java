class method_overriding {
    public static void age(int age){
        System.out.println(age);
    }
    public static void age(String no){
        System.out.println(no);
    }
    public static void main(String args[]){
        for(int i=0;i<1;i++){
            System.out.println(i+' ');
        }
    }
 

    }